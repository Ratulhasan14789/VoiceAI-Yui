from hashlib import md5, sha512, sha256, blake2b, blake2s, sha3_512, sha1, sha224, sha384, sha3_224, sha3_384, sha3_256
uipw=input('enter data: ')
print('\n md5')
print(md5(uipw.encode('utf-8')).hexdigest())
print('\n sha1')
print(sha1(uipw.encode('utf-8')).hexdigest())
print('\n sha224')
print(sha224(uipw.encode('utf-8')).hexdigest())
print('\n sha384')
print(sha384(uipw.encode('utf-8')).hexdigest())
print('\n sha256')
print(sha256(uipw.encode('utf-8')).hexdigest())
print('\n sha512')
print(sha512(uipw.encode('utf-8')).hexdigest())
print('\n sha3_224')
print(sha3_224(uipw.encode('utf-8')).hexdigest())
print('\n sha3_256')
print(sha3_256(uipw.encode('utf-8')).hexdigest())
print('\n sha3_384')
print(sha3_384(uipw.encode('utf-8')).hexdigest())
print('\n sha3_512')
print(sha3_512(uipw.encode('utf-8')).hexdigest())
print('\n blake2b')
print(blake2b(uipw.encode('utf-8')).hexdigest())
print('\n blake2s')
print(blake2s(uipw.encode('utf-8')).hexdigest())
